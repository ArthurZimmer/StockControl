    // botão de adicionar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tipo = String.valueOf(jComboBox1.getSelectedIndex());
        
        // cria um datasource para conexão com o banco
        DataSource ds = new DataSource();
        
        // instancia um objeto
        Fornecedores forn = new Fornecedores();
        daoFornecedores dao = new daoFornecedores(ds);

        daoCategorias dao1 = new daoCategorias(ds);
        Categorias cat = (Categorias) jComboBox1.getSelectedItem();

        // pega os dados do formulário
        forn.setFor_nome(jTextField1.getText());
        forn.setFor_tipo(cat.getIdCategoria());
        forn.setFor_endereco(jTextField3.getText());
        forn.setFor_complemento(jTextField4.getText());
        forn.setFor_bairro(jTextField5.getText());
        forn.setFor_cep(jTextField6.getText());
        forn.setFor_cidade(jTextField7.getText());
        forn.setFor_uf(jTextField8.getText());
        forn.setFor_telefone(jTextField9.getText());
        forn.setFor_cnpj(jTextField10.getText());
        forn.setFor_email(jTextField11.getText());
        forn.setFor_representante(jTextField12.getText());
        forn.setFor_fone_representante(jTextField13.getText());
        
        // executa a inserção que está no MunicipioDAO
        dao.create(forn);
        
        // fecha a janela
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

