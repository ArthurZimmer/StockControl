    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = jTable.getSelectedRow();
    
    if (linha == -1) {
        JOptionPane.showMessageDialog(null, "Produto não selecionado para excluir!");
        return;
    }
    
    int opcao = JOptionPane.showConfirmDialog(null, "Deseja excluir?", "Atenção", JOptionPane.YES_NO_OPTION);
    
    if (opcao == JOptionPane.YES_OPTION) {
        String codigo = jTable.getValueAt(linha, 0).toString();
        
        DataSource dataSource = new DataSource();
        daoProdutos dao = new daoProdutos(dataSource);
        
        dao.excluir(Integer.parseInt(codigo));
        
        // Atualiza a tabela após a exclusão
        readJTable();
    }
    }//GEN-LAST:event_jButton3ActionPerformed

