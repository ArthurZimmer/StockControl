    // popula JTable
    private void readJTable() {
    // Limpa a tabela antes de carregar novos dados
    DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
    modelo.setNumRows(0);

    try {
        // Cria o DataSource e o DAO de Produtos
        DataSource dataSource = new DataSource();
        daoProdutos produtosDao = new daoProdutos(dataSource);

        // Recupera a lista de todos os produtos
        ArrayList<Produtos> lista = produtosDao.readAll();

        // Popula a tabela com os produtos recuperados
        for (Produtos produto : lista) {
            modelo.addRow(new Object[]{
                produto.getIdProduto(),
                produto.getProd_categoria(),
                produto.getProd_descricao(),
                produto.getProd_validade(),
                produto.getProd_preco_custo(),
                produto.getProd_preco_venda(),
                produto.getIdFornecedor(),
                produto.getProd_quantidade(),
                produto.getProd_minimo()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao carregar produtos: " + e.getMessage());
    }
}


